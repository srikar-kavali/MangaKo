export function searchHardcodedManhwa(query) {
    const q = query.toLowerCase().trim();

    if (!q) {
        return manhwas;
    }

    const results = manhwas.filter(m => {
        const title = (m.title || m.title || '').toLowerCase();
        const description = (m.description || '').toLowerCase();
        const genres = (m.genres || []).join(' ').toLowerCase();

        // Title contains query
        if (title.includes(q)) return true;

        // Description or genres contain query
        if (description.includes(q) || genres.includes(q)) return true;

        return false;
    });

    return results;
}

export function getManhwaById(id) {
    return manhwas.find(m => m.id === id) || null;
}